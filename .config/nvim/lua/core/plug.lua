local ensure_packer = function()
  local fn = vim.fn
  local install_path = fn.stdpath('data')..'/site/pack/packer/start/packer.nvim'
  if fn.empty(fn.glob(install_path)) > 0 then
    fn.system({'git', 'clone', '--depth', '1', 'https://github.com/wbthomason/packer.nvim', install_path})
    vim.cmd [[packadd packer.nvim]]
    return true
  end
  return false
end

local packer_bootstrap = ensure_packer()

return require('packer').startup(function(use)
  use 'wbthomason/packer.nvim'                  -- this is the main package manager in nvim
  use 'nvim-tree/nvim-web-devicons'             -- this is for icons in nvim
  use 'theprimeagen/harpoon'                    -- this is for jumping to fav file
  use 'mbbill/undotree'                         -- this is for better undo support
  use 'tpope/vim-fugitive'                      -- this is for git intigration
  use 'nvim-treesitter/nvim-treesitter-context';  -- this is for better color support
  use { "catppuccin/nvim", as = "catppuccin" }  -- this is the default catppuccin color theme for nvim
  use {
    'nvim-treesitter/nvim-treesitter',
    run = function()
      local ts_update = require('nvim-treesitter.install').update({ with_sync = true })
      ts_update()
    end,
  }
  use {
    'VonHeikemen/lsp-zero.nvim',
    branch = 'v1.x',
    requires = {
      -- LSP Support
      {'neovim/nvim-lspconfig'},
      {'williamboman/mason.nvim'},
      {'williamboman/mason-lspconfig.nvim'},

      -- Autocompletion
      {'hrsh7th/nvim-cmp'},
